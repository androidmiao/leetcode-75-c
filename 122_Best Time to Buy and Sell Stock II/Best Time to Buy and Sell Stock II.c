/*
 * =====================================================================
 * LeetCode 122. Best Time to Buy and Sell Stock II
 * 買賣股票的最佳時機 II
 * 難度：Medium
 * 演算法：貪心法（Simple One Pass / Greedy）
 * 時間複雜度：O(n)  —— 單次遍歷陣列
 * 空間複雜度：O(1)  —— 僅使用常數額外空間
 * =====================================================================
 *
 * 【核心思路】
 *   允許無限次交易（但任一時刻最多持有 1 股）。
 *   貪心策略：只要「明天比今天貴」，就在今天買、明天賣，
 *   把所有正的連續差價累加起來，即可得到最大利潤。
 *
 *   數學上等價於「波谷-波峰法」（Peak-Valley）：
 *     profit = Σ (peak_k - valley_k)
 *   但簡化為逐日差價累加更好實作。
 *
 * 【逐步走過範例 1】 prices = [7, 1, 5, 3, 6, 4]
 *
 *   價格走勢圖（Y 軸 = 價格，X 軸 = 天數）：
 *
 *   7 | *                          ← 第 0 天：價格 7
 *   6 |                   *        ← 第 4 天：價格 6
 *   5 |         *                  ← 第 2 天：價格 5
 *   4 |                      *     ← 第 5 天：價格 4
 *   3 |             *              ← 第 3 天：價格 3
 *   2 |
 *   1 |     *                      ← 第 1 天：價格 1
 *   0 +---+---+---+---+---+---→ day
 *       0   1   2   3   4   5
 *
 *   逐日比較：
 *     day 0→1: prices[1]-prices[0] = 1-7 = -6  < 0 → 不買（跌）
 *     day 1→2: prices[2]-prices[1] = 5-1 = +4  > 0 → 買入 1 賣出 5，賺 4
 *     day 2→3: prices[3]-prices[2] = 3-5 = -2  < 0 → 不買（跌）
 *     day 3→4: prices[4]-prices[3] = 6-3 = +3  > 0 → 買入 3 賣出 6，賺 3
 *     day 4→5: prices[5]-prices[4] = 4-6 = -2  < 0 → 不買（跌）
 *
 *   max_profit = 0 + 4 + 3 = 7  ✓
 *
 * 【逐步走過範例 2】 prices = [1, 2, 3, 4, 5]
 *
 *   5 |                 *
 *   4 |             *
 *   3 |         *
 *   2 |     *
 *   1 | *
 *   0 +---+---+---+---+---→ day
 *       0   1   2   3   4
 *
 *   每天都漲：(2-1)+(3-2)+(4-3)+(5-4) = 1+1+1+1 = 4
 *   等同一次買入 1、賣出 5，利潤 = 4  ✓
 *
 * 【逐步走過範例 3】 prices = [7, 6, 4, 3, 1]
 *
 *   7 | *
 *   6 |     *
 *   5 |
 *   4 |         *
 *   3 |             *
 *   2 |
 *   1 |                 *
 *   0 +---+---+---+---+---→ day
 *       0   1   2   3   4
 *
 *   每天都跌：沒有任何正差價 → max_profit = 0  ✓
 *
 * =====================================================================
 */

int maxProfit(int* prices, int pricesSize) {
    /* 邊界情況：若陣列少於 2 個元素，無法進行任何交易 */
    if (pricesSize < 2) return 0;

    int max_profit = 0;  /* 累計最大利潤，初始化為 0 */

    /*
     * 貪心單次遍歷：
     * 從第 1 天開始，與前一天比較。
     * 只要今天的價格比昨天高（正差價），就把差價加入利潤。
     * 這等效於在每個波谷買入、在每個波峰賣出。
     */
    for (int i = 1; i < pricesSize; i++) {
        /* 若今天價格 > 昨天價格，代表有利可圖 */
        if (prices[i] > prices[i - 1]) {
            /* 累加正差價：等同於「昨天買入、今天賣出」 */
            max_profit += prices[i] - prices[i - 1];
        }
        /* 若今天價格 ≤ 昨天價格，不做任何操作（跳過下跌段） */
    }

    return max_profit;  /* 回傳累計的最大利潤 */
}
