/*
 * =========================================================
 * LeetCode 328: Odd Even Linked List
 * 難度: Medium (中等難度)
 *
 * 時間複雜度 (Time Complexity): O(n)
 *   - 其中 n 是鏈表中的節點數量
 *   - 我們需要遍歷整個鏈表一次
 *
 * 空間複雜度 (Space Complexity): O(1)
 *   - 只使用常數個指針變量（odd, even, starting_even）
 *   - 不需要額外的數據結構
 *
 * =========================================================
 * 演算法步驟 (Algorithm Walkthrough):
 * =========================================================
 *
 * 目標: 將鏈表按奇偶位置重新排列
 * - 奇數位置 (1st, 3rd, 5th...): 連接到一起
 * - 偶數位置 (2nd, 4th, 6th...): 連接到一起
 * - 最後將偶數位置鏈表連接到奇數位置鏈表的末尾
 *
 * =========================================================
 * 具體例子 (Concrete Example): [1,2,3,4,5]
 * =========================================================
 *
 * 初始狀態:
 *   head -> [1] -> [2] -> [3] -> [4] -> [5] -> NULL
 *   odd 指向 1 (奇數位置起點)
 *   even 指向 2 (偶數位置起點)
 *   starting_even 指向 2 (保存偶數鏈表的起點)
 *
 * ─────────────────────────────────────────────────────
 * 第1次迭代 (i=1, 處理第1和第2個節點):
 * ─────────────────────────────────────────────────────
 *
 * 步驟1: odd->next = even->next (1的next指向3)
 *   odd = 1, even = 2, even->next = 3
 *   結果: 1->3
 *
 *   odd: [1] -> [3] -> [4] -> [5] -> NULL
 *                ↑
 *
 *   even: [2] -> [3] -> [4] -> [5] -> NULL
 *                ↑
 *
 *   starting_even: [2] -> [3] -> [4] -> [5] -> NULL
 *
 * 步驟2: odd = odd->next (odd 移動到3)
 *   odd = 3
 *
 *   odd 指向: [3] -> [4] -> [5] -> NULL
 *
 * 步驟3: even->next = odd->next (2的next指向4)
 *   odd = 3, odd->next = 4
 *   結果: 2->4
 *
 *   odd: [3] -> [4] -> [5] -> NULL
 *                ↑
 *
 *   even: [2] -> [4] -> [5] -> NULL
 *                ↑
 *
 *   starting_even: [2] -> [4] -> [5] -> NULL
 *
 * 步驟4: even = even->next (even 移動到4)
 *   even = 4
 *
 *   even 指向: [4] -> [5] -> NULL
 *
 * ─────────────────────────────────────────────────────
 * 第2次迭代 (i=2, 處理第3和第4個節點):
 * ─────────────────────────────────────────────────────
 *
 * 步驟1: odd->next = even->next (3的next指向5)
 *   odd = 3, even = 4, even->next = 5
 *   結果: 3->5
 *
 *   odd: [3] -> [5] -> NULL
 *                ↑
 *
 *   even: [4] -> [5] -> NULL
 *                ↑
 *
 * 步驟2: odd = odd->next (odd 移動到5)
 *   odd = 5
 *
 *   odd 指向: [5] -> NULL
 *
 * 步驟3: even->next = odd->next (4的next指向NULL)
 *   odd = 5, odd->next = NULL
 *   結果: 4->NULL
 *
 *   even->next 為 NULL，觸發 break
 *
 * ─────────────────────────────────────────────────────
 * 第2次迭代後 (跳出while迴圈):
 * ─────────────────────────────────────────────────────
 *
 * 此時指針狀態:
 *   odd 指向 5 (奇數鏈表的末尾)
 *   starting_even 指向 2 (偶數鏈表的起點，已被修改)
 *
 * 當前的鏈表情況:
 *   奇數部分: 1 -> 3 -> 5 -> NULL
 *   偶數部分: 2 -> 4 -> NULL
 *   (但 starting_even 指向的 [2] 的 next 已指向偶數鏈表)
 *
 * 步驟: odd->next = starting_even
 *   將奇數鏈表的末尾(5)連接到偶數鏈表的起點(2)
 *
 *   結果: 1 -> 3 -> 5 -> 2 -> 4 -> NULL
 *
 * ─────────────────────────────────────────────────────
 * 最終結果:
 * ─────────────────────────────────────────────────────
 *
 * head -> [1] -> [3] -> [5] -> [2] -> [4] -> NULL
 *         ↑     ↑     ↑     ↑     ↑
 *        奇1   奇3   奇5   偶2   偶4
 *
 * =========================================================
 */

#include "solution.h"

/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     struct ListNode *next;
 * };
 */
struct ListNode* oddEvenList(struct ListNode* head) {
    /* 邊界檢查: 如果鏈表為空或只有一個節點，直接返回 */
    /* 原因: 沒有偶數位置節點需要重新排列 */
    if (!head || !head->next) return head;

    /* odd: 指向奇數位置節點的指針，初始指向第1個節點 (位置1) */
    struct ListNode* odd = head;

    /* even: 指向偶數位置節點的指針，初始指向第2個節點 (位置2) */
    struct ListNode* even = head->next;

    /* starting_even: 保存偶數鏈表起點的常量指針 */
    /* 用途: 在最後將整個偶數鏈表連接到奇數鏈表末尾 */
    /* 為什麼使用 const: 確保指針地址不被修改，只跟蹤偶數鏈表的起點 */
    struct ListNode* const starting_even = head->next;

    /* while (odd): 持續迴圈直到奇數位置指針變為 NULL */
    /* 不變式 (Invariant): */
    /*   - odd 總是指向奇數位置的節點 */
    /*   - even 總是指向偶數位置的節點 */
    /*   - 奇數部分: 1 -> 3 -> 5 -> ... -> odd */
    /*   - 偶數部分: 2 -> 4 -> 6 -> ... -> even (被修改，指向新的偶數節點) */
    while (odd) {
        /* odd->next = even->next */
        /* 操作: 讓當前奇數節點指向下一個奇數節點 */
        /* 在這之前，odd->next 指向偶數節點，現在指向下下個節點(下一個奇數位置) */
        /* 例子: 1(odd)->2(even)->3(next odd)->4... 改成 1->3->4... */
        odd->next = even->next;

        /* 檢查: 是否還有下一個奇數節點存在 */
        /* 條件 !odd->next 表示已經沒有更多節點，退出迴圈 */
        /* 這發生在總節點數為偶數的情況下 (最後一個節點是偶數位置) */
        if (!odd->next) break;

        /* odd = odd->next */
        /* 指針移動: 將 odd 移至下一個奇數位置節點 */
        /* 不變式維護: odd 始終指向奇數序列中的一個節點 */
        odd = odd->next;

        /* even->next = odd->next */
        /* 操作: 讓當前偶數節點指向下一個偶數節點 */
        /* 在這之前，even->next 指向奇數節點，現在指向下下個節點(下一個偶數位置) */
        /* 例子: ...(odd) 2(even)->3->4(next even)->5... 改成 2->4->5... */
        even->next = odd->next;

        /* 檢查: 是否還有下一個偶數節點存在 */
        /* 條件 !even->next 表示已經沒有更多節點，退出迴圈 */
        /* 這發生在總節點數為奇數的情況下 (最後一個節點是奇數位置) */
        if (!even->next) break;

        /* even = even->next */
        /* 指針移動: 將 even 移至下一個偶數位置節點 */
        /* 不變式維護: even 始終指向偶數序列中的一個節點 */
        even = even->next;
    }

    /* 連接奇偶鏈表 */
    /* odd 現在指向奇數序列的最後一個節點 */
    /* starting_even 指向偶數序列的第一個節點 (值為2的節點) */
    /* 此時偶數序列已被重新組織，指向正確的偶數位置節點 */
    /* 例子: 奇數部分末尾是5，偶數部分起點是2，連接後得到 ...5->2->... */
    odd->next = starting_even;

    /* 返回原始頭節點，整個鏈表已被重新排列 */
    /* 返回值: 指向重新排列後的鏈表頭部 (第1個奇數位置節點) */
    return head;
}
