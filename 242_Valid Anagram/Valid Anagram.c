/*
 * LeetCode 242. 有效的異位詞
 * 難度：Easy
 *
 * 問題描述：
 * 給定兩個字符串 s 和 t，
 * 判斷 t 是否是 s 的異位詞。
 * 異位詞是指由相同字符組成但順序不同的單詞。
 *
 * 演算法：字符頻率計數
 *
 * 核心思想：
 * - 創建一個頻率計數數組，用於統計26個小寫字母
 * - 遍歷s，對每個字符的頻率加1
 * - 遍歷t，對每個字符的頻率減1
 * - 如果t是s的異位詞，最後所有字符的頻率應該為0
 * - 檢查是否有非零頻率，有則返回false，都是0則返回true
 *
 * 時間複雜度分析：
 * - 時間複雜度：O(n)，其中n是字符串長度
 *   遍歷s一次 + 遍歷t一次 + 檢查數組一次
 * - 空間複雜度：O(1)，固定26個字母的數組
 */

#include <stdlib.h>
#include <string.h>
#include <stdbool.h>

bool isAnagram(char* s, char* t) {
    /* 獲取字符串長度 */
    int s_len = strlen(s);
    int t_len = strlen(t);

    /* 異位詞必須有相同的長度 */
    /* 否則無法由相同的字符組成 */
    if (s_len != t_len) {
        return false;
    }

    /* 創建頻率計數數組，用於26個小寫字母 */
    int freq[26] = {0};

    /* 第一次遍歷：掃描字符串 s，統計每個字符的頻率 */
    /* 對於字符c，我們將其映射到 [0, 25] 範圍 */
    for (int i = 0; i < s_len; i++) {
        /* s[i] - 'a' 將字符轉換為 0-25 的索引 */
        /* 例如：'a' -> 0, 'b' -> 1, ..., 'z' -> 25 */
        freq[s[i] - 'a']++;
    }

    /* 第二次遍歷：掃描字符串 t，減去每個字符的頻率 */
    /* 如果t是s的異位詞，最後所有頻率應該變為0 */
    for (int i = 0; i < t_len; i++) {
        freq[t[i] - 'a']--;
    }

    /* 檢查頻率數組，看是否所有值都為0 */
    for (int i = 0; i < 26; i++) {
        /* 如果任何字符的頻率非零，說明兩個字符串的字符組成不同 */
        if (freq[i] != 0) {
            return false;
        }
    }

    /* 所有頻率都為0，說明兩個字符串由相同的字符組成 */
    return true;
}
