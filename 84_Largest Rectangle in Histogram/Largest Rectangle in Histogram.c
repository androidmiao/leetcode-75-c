/*
 * LeetCode 84. Largest Rectangle in Histogram（柱狀圖中最大的矩形）
 * 難度：Hard
 * 演算法：單調遞增棧 + 哨兵技巧 (Monotonic Increasing Stack with Sentinels)
 * 時間複雜度：O(n) — 每個元素最多推入和彈出各一次
 * 空間複雜度：O(n) — 堆疊和哨兵陣列各佔 O(n)
 *
 * ============================================================
 * 核心觀念
 * ============================================================
 * 對於每根柱子 h，我們想知道「以 h 為最矮高度，能向左右延伸多遠」。
 * 左邊界 = 左邊第一根比 h 矮的柱子索引 + 1
 * 右邊界 = 右邊第一根比 h 矮的柱子索引 - 1
 * 面積 = h × (右邊界 - 左邊界 + 1)
 *
 * 單調遞增棧在 pop 時自然找到左右邊界：
 *   - pop 時的 i 就是「右邊第一根比被 pop 柱子矮」的位置
 *   - pop 後的棧頂就是「左邊第一根比被 pop 柱子矮」的位置
 *
 * ============================================================
 * 哨兵技巧 (Sentinel Technique)
 * ============================================================
 * 在原始陣列前後各加一個高度為 0 的哨兵柱子：
 *   new_heights = [0] + heights + [0]
 *
 * 左哨兵（索引 0）：確保棧永遠不為空，免去「棧空時的特判」
 * 右哨兵（索引 n+1）：觸發所有殘留在棧中的柱子被 pop 並計算面積
 *
 * ============================================================
 * 範例推演：heights = [2, 1, 5, 6, 2, 3]
 * ============================================================
 *
 * new_heights (加哨兵後) = [0, 2, 1, 5, 6, 2, 3, 0]
 * 索引:                     0  1  2  3  4  5  6  7
 *
 * 步驟  i   new_h[i]  動作                          棧(索引)     max_area
 * ─────────────────────────────────────────────────────────────────────
 *  1    0      0      push 0                        [0]            0
 *  2    1      2      2>0, push 1                   [0,1]          0
 *  3    2      1      1<2, pop 1:                   [0]            0→2
 *                       h=2, w=2-0-1=1, area=2
 *                     1≥0? push 不了? 1>0, push 2   [0,2]          2
 *  4    3      5      5>1, push 3                   [0,2,3]        2
 *  5    4      6      6>5, push 4                   [0,2,3,4]      2
 *  6    5      2      2<6, pop 4:                   [0,2,3]        2→6
 *                       h=6, w=5-3-1=1, area=6
 *                     2<5, pop 3:                   [0,2]          6→10
 *                       h=5, w=5-2-1=2, area=10    ← 最大！
 *                     2≥1, push 5                   [0,2,5]       10
 *  7    6      3      3>2, push 6                   [0,2,5,6]     10
 *  8    7      0      0<3, pop 6:                   [0,2,5]       10
 *                       h=3, w=7-5-1=1, area=3
 *                     0<2, pop 5:                   [0,2]         10
 *                       h=2, w=7-2-1=4, area=8
 *                     0<1, pop 2:                   [0]           10
 *                       h=1, w=7-0-1=6, area=6
 *                     0≥0, push 7                   [0,7]         10
 *
 * 結果：max_area = 10
 *
 *   柱狀圖視覺化 (原始 heights = [2,1,5,6,2,3]):
 *
 *        ┌───┐
 *   6    │   │
 *   5  ┌─┤   │
 *   4  │ │   │         ┌───┐
 *   3  │ │   │   ┌─────┤   │
 *   2 ┌┤ │   │   │     │   │
 *   1 ││ │   │   │     │   │
 *     ├┼─┼───┼───┼─────┼───┤
 *     [2][1] [5] [6]  [2] [3]
 *      0  1   2   3    4   5
 *
 *        ┌───┐
 *      ┌─╋━━━╋─────────────┐  ← 最大矩形：高度5，寬度2
 *      │ ╋━━━╋             │     面積 = 5 × 2 = 10
 *      │ │   │         ┌───┤
 *      │ │   │   ┌─────┤   │
 *     ┌┤ │   │   │     │   │
 *     ││ │   │   │     │   │
 *     ├┼─┼───┼───┼─────┼───┤
 *     [2][1] [5] [6]  [2] [3]
 */

#include <stdlib.h>

int largestRectangleArea(int* heights, int heightsSize) {
    /* 配置堆疊空間，最多容納 heightsSize + 2 個索引（含兩個哨兵） */
    int* stack = (int*)malloc((heightsSize + 2) * sizeof(int));
    int top = -1;          /* 棧頂指標，-1 表示空棧 */
    int max_area = 0;      /* 追蹤目前找到的最大面積 */

    /* 建立含哨兵的新高度陣列：[0, heights[0..n-1], 0] */
    int* new_heights = (int*)malloc((heightsSize + 2) * sizeof(int));
    new_heights[0] = 0;    /* 左哨兵：高度 0，保證棧不為空 */
    for (int i = 0; i < heightsSize; i++) {
        new_heights[i + 1] = heights[i];   /* 複製原始高度到偏移 +1 的位置 */
    }
    new_heights[heightsSize + 1] = 0;      /* 右哨兵：高度 0，強制清空棧 */
    int total = heightsSize + 2;            /* 含哨兵的總長度 */

    for (int i = 0; i < total; i++) {
        /*
         * 維護單調遞增棧的不變量：
         * 當新柱子高度 < 棧頂柱子高度時，彈出棧頂並計算面積。
         * 被彈出的柱子是「以該高度為最矮」的矩形的高度。
         */
        while (top >= 0 && new_heights[stack[top]] > new_heights[i]) {
            int h_index = stack[top--];     /* 彈出棧頂，取得該柱子的索引 */
            int h = new_heights[h_index];   /* 矩形的高度 = 被彈出柱子的高度 */
            /*
             * 寬度計算：
             *   右邊界 = i（當前索引，第一根比 h 矮的柱子）
             *   左邊界 = stack[top]（彈出後的新棧頂，左邊第一根比 h 矮的柱子）
             *   寬度 = i - stack[top] - 1
             *
             *   若棧已空（top < 0），理論上不會發生，因為左哨兵永遠在棧底
             */
            int w = (top >= 0) ? (i - stack[top] - 1) : 0;
            int area = h * w;               /* 以 h 為高度的最大矩形面積 */
            if (area > max_area) {
                max_area = area;            /* 更新全域最大面積 */
            }
        }
        stack[++top] = i;   /* 將當前索引推入棧中，維持遞增順序 */
    }

    free(stack);            /* 釋放堆疊記憶體 */
    free(new_heights);      /* 釋放哨兵陣列記憶體 */
    return max_area;        /* 回傳最大矩形面積 */
}
