/*
 * 724. Find Pivot Index (Easy)
 *
 * 時間複雜度: O(n) — 兩次掃描數組
 * 空間複雜度: O(1) — 只用常數額外空間
 *
 * ===================================================================
 * 演算法說明（繁體中文）
 * ===================================================================
 *
 * 核心思想：
 *   樞紐點（pivot index）的定義是：
 *     樞紐點左邊所有元素之和 == 樞紐點右邊所有元素之和
 *
 *   一次掃描求出全部元素的總和 right_sum，
 *   然後從左到右逐一檢查每個位置：
 *     在第 i 位置：
 *       - left_sum = [0 .. i-1] 的總和
 *       - right_sum = [i+1 .. n-1] 的總和
 *
 *   當 left_sum == right_sum 時，i 就是樞紐點。
 *
 *   技巧：用累積的方式，每次先將 nums[i] 從右邊總和扣掉，
 *         再比較 left_sum 與新的 right_sum，
 *         最後把 nums[i] 加進 left_sum，
 *         這樣可以在一次循環內完成檢查。
 *
 * ===================================================================
 * 範例走一次：nums = [1,7,3,6,5,6]
 * ===================================================================
 *
 * 初始化：求全部元素和
 *   right_sum = 1 + 7 + 3 + 6 + 5 + 6 = 28
 *   left_sum = 0
 *
 * 開始檢查每個位置：
 *
 * i=0, nums[0]=1
 *   right_sum -= nums[0]  →  right_sum = 28 - 1 = 27
 *   比較：left_sum (0) == right_sum (27)？否
 *   left_sum += nums[0]  →  left_sum = 0 + 1 = 1
 *
 * i=1, nums[1]=7
 *   right_sum -= nums[1]  →  right_sum = 27 - 7 = 20
 *   比較：left_sum (1) == right_sum (20)？否
 *   left_sum += nums[1]  →  left_sum = 1 + 7 = 8
 *
 * i=2, nums[2]=3
 *   right_sum -= nums[2]  →  right_sum = 20 - 3 = 17
 *   比較：left_sum (8) == right_sum (17)？否
 *   left_sum += nums[2]  →  left_sum = 8 + 3 = 11
 *
 * i=3, nums[3]=6
 *   right_sum -= nums[3]  →  right_sum = 17 - 6 = 11
 *   比較：left_sum (11) == right_sum (11)？是 ✓
 *   返回樞紐點索引：3
 *
 * ===================================================================
 * 狀態變化圖（ASCII）
 * ===================================================================
 *
 * 數組：  [1,  7,  3,  6,  5,  6]
 *   索引： 0   1   2   3   4   5
 *
 * 初始狀態（全部和 = 28）：
 *   left_sum = 0      nums[i]    right_sum = 28
 *
 * i=0:
 *   [1] | 1 | [7, 3, 6, 5, 6]
 *        0  ≠  27
 *   left_sum=1
 *
 * i=1:
 *   [1, 7] | 3 | [6, 5, 6]
 *            8  ≠  20
 *   left_sum=8
 *
 * i=2:
 *   [1, 7, 3] | 6 | [5, 6]
 *               11 ≠  17
 *   left_sum=11
 *
 * i=3:
 *   [1, 7, 3, 6] | 5 | [6]
 *                  11 == 11  ← 樞紐點！
 *   返回 3
 *
 */

#include "solution.h"

int pivotIndex(int* nums, int nums_size) {
    /* left_sum：樞紐點左邊所有元素的總和（初始為 0） */
    /* right_sum：首先初始化為全部元素的總和 */
    int left_sum = 0, right_sum = 0;

    /* 第一遍掃描：計算全部元素的總和 */
    for (int i = 0; i < nums_size; ++i) {
        right_sum += nums[i];  /* 累積所有元素，以便後續逐個扣除 */
    }

    /* 第二遍掃描：從左到右檢查每個位置是否為樞紐點 */
    for (int i = 0; i < nums_size; ++i) {
        /*
         * 關鍵步驟：先把 nums[i] 從 right_sum 扣掉
         * 此時 right_sum 代表 [i+1 .. end] 的總和
         * left_sum 代表 [0 .. i-1] 的總和
         */
        right_sum -= nums[i];

        /* 檢查樞紐點條件：左邊之和 == 右邊之和 */
        if (left_sum == right_sum) {
            /* 找到樞紐點，返回其索引 */
            return i;
        }

        /*
         * 把 nums[i] 加入 left_sum，為下一次迭代做準備
         * 不變量：迴圈結尾時 left_sum == [0..i] 的總和
         */
        left_sum += nums[i];
    }

    /* 若迴圈結束仍未找到樞紐點，返回 -1 表示不存在 */
    return -1;
}
