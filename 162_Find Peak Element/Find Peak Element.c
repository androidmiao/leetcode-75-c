/*
 * 162. Find Peak Element
 * Difficulty: Medium
 * Time Complexity:  O(log n)  — 每次砍半搜尋空間
 * Space Complexity: O(1)      — 迭代版本，只用常數指標
 *
 * ─────────────────────────────────────────────────────────────────
 * 演算法：Iterative Binary Search（迭代二分搜尋）
 * ─────────────────────────────────────────────────────────────────
 *
 * 核心想法
 * --------
 * 題目保證 nums[-1] = nums[n] = -∞，所以：
 *   - 若 nums[mid] > nums[mid+1]，峰值在左半段（含 mid）
 *     因為 mid 要麼本身就是峰值，要麼往左必有峰值
 *   - 若 nums[mid] < nums[mid+1]，峰值在右半段（不含 mid）
 *     因為右邊數值更大，往右必有峰值
 *
 * 為何此邏輯正確？
 * ----------------
 * 任何時刻，搜尋空間 [l, r] 必定包含至少一個峰值。
 * 這是因為：兩端點都是比邊界（-∞）大的實際值，
 * 且陣列為有限長度，故一定存在局部最大值。
 *
 * ASCII 示意圖
 * -----------
 * 例：nums = [1, 2, 3, 1]
 *
 *   索引:   0   1   2   3
 *   值:    [1] [2] [3] [1]
 *
 *   第 1 輪: l=0, r=3, mid=1
 *            nums[1]=2, nums[2]=3
 *            2 < 3 → 峰值在右半段 → l = mid+1 = 2
 *
 *   第 2 輪: l=2, r=3, mid=2
 *            nums[2]=3, nums[3]=1
 *            3 > 1 → 峰值在左半段（含mid）→ r = mid = 2
 *
 *   l == r == 2 → 退出迴圈，回傳 2
 *
 * 例：nums = [1, 2, 1, 3, 5, 6, 4]
 *
 *   第 1 輪: l=0, r=6, mid=3, nums[3]=3, nums[4]=5
 *            3 < 5 → l = 4
 *   第 2 輪: l=4, r=6, mid=5, nums[5]=6, nums[6]=4
 *            6 > 4 → r = 5
 *   第 3 輪: l=4, r=5, mid=4, nums[4]=5, nums[5]=6
 *            5 < 6 → l = 5
 *   l == r == 5 → 回傳 5（nums[5]=6 是峰值）
 * ─────────────────────────────────────────────────────────────────
 */
int findPeakElement(int* nums, int numsSize) {
    int l = 0;           /* 搜尋區間左端點 */
    int r = numsSize - 1; /* 搜尋區間右端點 */

    /*
     * 迴圈條件 l < r：當 l == r 時，區間只剩一個元素，
     * 根據不變量它必定是峰值，可直接回傳。
     */
    while (l < r) {
        int mid = l + (r - l) / 2; /* 避免整數溢位 */

        if (nums[mid] > nums[mid + 1]) {
            /* mid 可能是峰值，或峰值在 mid 左邊 → 縮小右邊界到 mid */
            r = mid;
        } else {
            /* nums[mid] < nums[mid+1]，右邊更大，峰值在右半段 → 縮小左邊界 */
            l = mid + 1;
        }
    }

    /* 此時 l == r，必為峰值 */
    return l;
}
