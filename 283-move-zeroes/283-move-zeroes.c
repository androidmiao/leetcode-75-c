/*
 * 283. Move Zeroes (Easy)
 * Two Pointers — Approach #3 (Optimal Swap)
 *
 * 時間複雜度: O(n) — 單次掃描，寫入次數 = 非零元素數量
 * 空間複雜度: O(1) — 只用常數額外空間
 *
 * ===================================================================
 * 演算法說明（繁體中文）
 * ===================================================================
 *
 * 核心思想：
 *   用兩個指標 cur（掃描指標）和 lastNonZero（慢指標），
 *   維持以下不變量（invariant）：
 *     1. [0 .. lastNonZero-1] 全部是非零，且保持原始相對順序
 *     2. [lastNonZero .. cur-1] 全部是零
 *     3. [cur .. numsSize-1] 尚未掃描
 *
 *   遇到非零 → 與 lastNonZero 交換 → 兩邊各推進一格
 *   遇到零   → 只推進 cur
 *
 *   這樣只在「真正需要搬動」時才寫入，最佳情況（全部非零）
 *   每個元素自己跟自己交換（可加 if 判斷跳過），最差也只有 n 次寫入。
 *
 * ===================================================================
 * 範例走一次：nums = [0, 1, 0, 3, 12]
 * ===================================================================
 *
 * 初始狀態：
 *   index:       0    1    2    3    4
 *   value:      [0,   1,   0,   3,  12]
 *                ^
 *               cur
 *                ^
 *            lastNonZero = 0
 *
 * Step 1: cur=0, nums[0]=0 → 是零，跳過
 *   index:       0    1    2    3    4
 *   value:      [0,   1,   0,   3,  12]
 *                     ^
 *                    cur=1
 *                ^
 *            lastNonZero=0
 *
 * Step 2: cur=1, nums[1]=1 → 非零，swap(nums[0], nums[1])
 *   交換前: [0,  1,  0,  3, 12]
 *   交換後: [1,  0,  0,  3, 12]
 *   lastNonZero++ → 1, cur++ → 2
 *
 *   index:       0    1    2    3    4
 *   value:      [1,   0,   0,   3,  12]
 *                          ^
 *                         cur=2
 *                     ^
 *                 lastNonZero=1
 *
 * Step 3: cur=2, nums[2]=0 → 是零，跳過
 *   index:       0    1    2    3    4
 *   value:      [1,   0,   0,   3,  12]
 *                               ^
 *                              cur=3
 *                     ^
 *                 lastNonZero=1
 *
 * Step 4: cur=3, nums[3]=3 → 非零，swap(nums[1], nums[3])
 *   交換前: [1,  0,  0,  3, 12]
 *   交換後: [1,  3,  0,  0, 12]
 *   lastNonZero++ → 2, cur++ → 4
 *
 *   index:       0    1    2    3    4
 *   value:      [1,   3,   0,   0,  12]
 *                                    ^
 *                                   cur=4
 *                          ^
 *                      lastNonZero=2
 *
 * Step 5: cur=4, nums[4]=12 → 非零，swap(nums[2], nums[4])
 *   交換前: [1,  3,  0,  0, 12]
 *   交換後: [1,  3, 12,  0,  0]
 *   lastNonZero++ → 3, cur++ → 5 (結束)
 *
 * 最終結果：[1, 3, 12, 0, 0] ✓
 *
 * ===================================================================
 * 雙指標移動示意圖（ASCII）
 * ===================================================================
 *
 *   cur ───────────────────────────────────────────►
 *   0       1       2       3       4
 *  ┌───┐  ┌───┐  ┌───┐  ┌───┐  ┌───┐
 *  │ 0 │  │ 1 │  │ 0 │  │ 3 │  │ 12│   初始
 *  └───┘  └───┘  └───┘  └───┘  └───┘
 *    ▲
 *  lastNonZero
 *
 *  ┌───┐  ┌───┐  ┌───┐  ┌───┐  ┌───┐
 *  │ 1 │  │ 0 │  │ 0 │  │ 3 │  │ 12│   swap(0,1) 後
 *  └───┘  └───┘  └───┘  └───┘  └───┘
 *           ▲
 *         lastNonZero
 *
 *  ┌───┐  ┌───┐  ┌───┐  ┌───┐  ┌───┐
 *  │ 1 │  │ 3 │  │ 0 │  │ 0 │  │ 12│   swap(1,3) 後
 *  └───┘  └───┘  └───┘  └───┘  └───┘
 *                  ▲
 *              lastNonZero
 *
 *  ┌───┐  ┌───┐  ┌───┐  ┌───┐  ┌───┐
 *  │ 1 │  │ 3 │  │ 12│  │ 0 │  │ 0 │   swap(2,4) 後 ← 最終
 *  └───┘  └───┘  └───┘  └───┘  └───┘
 *                         ▲
 *                     lastNonZero=3
 *
 * 一句話記憶：cur 負責「找非零」，lastNonZero 負責「放非零」，
 *            用 swap 確保零自動被推往後方。
 */

void moveZeroes(int* nums, int numsSize) {
    int lastNonZero = 0;  /* 慢指標：下一個非零元素應放置的位置 */

    for (int cur = 0; cur < numsSize; cur++) {  /* cur：快指標，逐一掃描每個元素 */
        if (nums[cur] != 0) {
            /* 遇到非零元素 → 與 lastNonZero 位置交換 */
            /* 若 cur == lastNonZero，等於自己跟自己交換，無副作用 */
            int tmp = nums[lastNonZero];       /* 暫存慢指標位置的值（一定是 0 或尚未處理） */
            nums[lastNonZero] = nums[cur];     /* 把非零值搬到慢指標位置 */
            nums[cur] = tmp;                   /* 把原本的 0（或同值）放回 cur 位置 */
            lastNonZero++;                     /* 慢指標右移，準備接收下一個非零值 */
        }
        /* 若 nums[cur] == 0，不做任何事，cur 繼續前進 */
        /* 不變量維持：[0..lastNonZero-1] 非零，[lastNonZero..cur] 全零 */
    }
}
