/*
 * 287. 尋找重複的數字 (Find the Duplicate Number)
 * 難度: Medium | 時間複雜度: O(n) | 空間複雜度: O(1)
 *
 * 演算法: Floyd's Tortoise and Hare 週期檢測
 * ==========================================
 *
 * 核心思想:
 * 將陣列視為一個鏈表結構，其中 nums[i] 指向下一個位置。
 * 由於有重複元素，必定存在一個週期 (cycle)。
 * 使用龜和兔快慢指標找到週期入口，即為重複的數字。
 *
 * 演算法步驟:
 * 1. 第一階段 (Phase 1 - 尋找週期)
 *    - 龜 (tortoise): 每次走 1 步
 *    - 兔 (hare): 每次走 2 步
 *    - 當龜和兔相遇時，必定在週期內某處
 *
 * 2. 第二階段 (Phase 2 - 尋找週期入口)
 *    - 龜和兔相遇後，將兔重置到起點 nums[0]
 *    - 龜保持當前位置，兔回到起點
 *    - 龜和兔都改為每次走 1 步
 *    - 當龜和兔再次相遇時，相遇點即為週期入口 (重複數字)
 *
 * 數學原理:
 * 假設：
 *   - a = 從起點到週期入口的距離
 *   - b = 週期入口到相遇點的距離
 *   - c = 相遇點回到週期入口的距離 (b + c = 週期長度)
 *
 * 在第一階段，龜走了 a+b 步，兔走了 a+b+k*週期長度 步
 * 兔總步數 = 龜步數 * 2，所以：
 *   a+b+k*(b+c) = 2*(a+b)
 *   => a = k*(b+c) - b = (k-1)*(b+c) + (b+c-b) = (k-1)*週期 + c
 *
 * 這意味著從起點走 a 步到週期入口，等於從相遇點走 c 步回到週期入口
 *
 * 範例演練: nums = [1,3,4,2,2]
 * ===============================
 * 索引:    0  1  2  3  4
 * 值:      1  3  4  2  2
 *
 * 鏈表視角 (nums[i] -> nums[nums[i]]):
 *   索引0 -> 索引1 -> 索引3 -> 索引2 -> 索引4 -> 索引2 (週期！)
 *
 * 第一階段 (龜兔尋找相遇點):
 * ========================
 * 迭代 0: tortoise = nums[nums[0]] = nums[1] = 3
 *         hare = nums[nums[nums[0]]] = nums[nums[1]] = nums[3] = 2
 *
 * 迭代 1: tortoise = nums[3] = 2
 *         hare = nums[nums[2]] = nums[4] = 2
 *         => 相遇！都在索引 2
 *
 * 第二階段 (龜兔尋找週期入口):
 * =========================
 * 兔重置: hare = nums[0] = 1
 *
 * 迭代 0: tortoise = nums[2] = 4
 *         hare = nums[1] = 3
 *
 * 迭代 1: tortoise = nums[4] = 2
 *         hare = nums[3] = 2
 *         => 相遇！週期入口在索引 2，對應值 2（重複元素）
 *
 * 視覺化圖表:
 * ===========
 *
 * 原始陣列結構:
 * 索引: 0   1   2   3   4
 * 值:   1   3   4   2   2
 *       ↓   ↓   ↑←─┐ ↑   ↓
 *       └───┘   └──┴─┴───┘ (週期存在)
 *
 * 鏈表表示 (值作為下一個索引):
 * 起點(0) -> 1 -> 3 -> 2 -> 4 -> 2 (重複!) -> 4 -> 2 -> ... (循環)
 *                      ↑←────────────┘
 *                    週期入口 (數字 2)
 *
 * 第一階段演進:
 * ============
 * 步驟  tortoise (龜)  hare (兔)     說明
 * ----  -----------  -----------   ------------------
 * 0     nums[1]=3    nums[3]=2      龜走1步→3, 兔走2步→2
 * 1     nums[3]=2    nums[2]=4      龜走1步→2, 兔走2步→4
 *       ✓ 相遇點達到 (都在值 2 的位置)
 *
 * 第二階段演進:
 * ============
 * 重置: tortoise=2 (保持), hare=nums[0]=1 (回到起點)
 *
 * 步驟  tortoise (龜)  hare (兔)     說明
 * ----  -----------  -----------   ------------------
 * 0     nums[2]=4    nums[1]=3      龜走1步→4, 兔走1步→3
 * 1     nums[4]=2    nums[3]=2      龜走1步→2, 兔走1步→2
 *       ✓ 相遇！此時相遇於重複數字的位置 2
 */

#include "solution.h"

int findDuplicate(int* nums, int n) {
    // 第一階段: 龜兔快慢指標尋找週期內的相遇點
    // 龜每次走一步，兔每次走兩步。假設存在週期，它們必定相遇
    // 初始化時龜和兔都已各自走了一步和兩步（比標準做法提前一步）
    int tortoise = nums[nums[0]];  // 龜: 從起點開始走 1 步後的位置
    int hare = nums[nums[nums[0]]];  // 兔: 從起點開始走 2 步後的位置

    // 持續移動直到龜兔相遇
    // 不變式: 龜在週期內或週期外，兔在週期內，相遇點位於週期內
    while (tortoise != hare) {
        tortoise = nums[tortoise];  // 龜走一步: 當前值作為下一個索引
        hare = nums[nums[hare]];    // 兔走兩步: 走兩次索引跳躍
    }

    // 第二階段: 尋找週期入口（重複數字所在位置）
    // 此時龜在週期內的某個位置，兔也在相同位置
    // 重置兔到起點，龜保持當前位置，都改為一次走一步
    hare = nums[0];  // 兔回到起點 nums[0]

    // 持續移動直到龜兔再次相遇
    // 不變式: 從起點到週期入口的距離 = 從相遇點到週期入口的距離
    while (tortoise != hare) {
        tortoise = nums[tortoise];  // 龜每次走一步
        hare = nums[hare];          // 兔每次走一步（與龜同步）
    }

    // 相遇點即為週期入口，對應重複的數字
    return tortoise;  // 返回重複數字（週期入口的值）
}
