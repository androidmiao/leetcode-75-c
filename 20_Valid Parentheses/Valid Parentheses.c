/*
 * LeetCode 20. Valid Parentheses（有效的括號）
 * 難度：Easy
 * 演算法：堆疊（Stack）— 推入開括號，遇到閉括號時彈出並比對
 * 時間複雜度：O(n)，空間複雜度：O(n)
 *
 * =====================================================================
 * 演算法步驟說明（以 s = "{[()]}" 為例）
 * =====================================================================
 *
 * 步驟 0：初始化空堆疊，top = -1
 *
 *   字串：  {  [  (  )  ]  }
 *   索引：  0  1  2  3  4  5
 *
 *   堆疊（底→頂）：[ 空 ]
 *
 * 步驟 1：讀取 s[0] = '{'，是開括號 → 推入堆疊
 *
 *   堆疊：[ { ]
 *          ↑ top=0
 *
 * 步驟 2：讀取 s[1] = '['，是開括號 → 推入堆疊
 *
 *   堆疊：[ {  [ ]
 *             ↑ top=1
 *
 * 步驟 3：讀取 s[2] = '('，是開括號 → 推入堆疊
 *
 *   堆疊：[ {  [  ( ]
 *                ↑ top=2
 *
 * 步驟 4：讀取 s[3] = ')'，是閉括號
 *         → 彈出堆疊頂部 '('，檢查 ')' 對應 '(' ✓ 匹配
 *
 *   堆疊：[ {  [ ]
 *             ↑ top=1
 *
 * 步驟 5：讀取 s[4] = ']'，是閉括號
 *         → 彈出堆疊頂部 '['，檢查 ']' 對應 '[' ✓ 匹配
 *
 *   堆疊：[ { ]
 *          ↑ top=0
 *
 * 步驟 6：讀取 s[5] = '}'，是閉括號
 *         → 彈出堆疊頂部 '{'，檢查 '}' 對應 '{' ✓ 匹配
 *
 *   堆疊：[ 空 ]   top=-1
 *
 * 結果：堆疊為空（top == -1）→ 回傳 true
 *
 * =====================================================================
 * 失敗範例（s = "([)]"）
 * =====================================================================
 *
 *   步驟 1：'(' → 推入  堆疊：[ ( ]
 *   步驟 2：'[' → 推入  堆疊：[ (  [ ]
 *   步驟 3：')' → 彈出 '['，但 ')' 對應的應該是 '(' ✗ 不匹配
 *           → 回傳 false
 *
 * =====================================================================
 * 堆疊狀態變化示意圖（s = "{[()]}"）
 * =====================================================================
 *
 *   top → |   |   | ( |   |   |   |
 *         |   | [ | [ | [ |   |   |
 *         | { | { | { | { | { |   |
 *         +---+---+---+---+---+---+
 *   字元：  {   [   (   )   ]   }
 *   動作： push push push pop pop pop
 */

#include <stdlib.h>
#include <stdbool.h>
#include <string.h>

bool isValid(char* s) {
    int len = strlen(s);                /* 計算字串長度 */
    if (len == 0) return true;          /* 空字串視為有效 */
    if (len % 2 == 1) return false;     /* 奇數長度不可能配對成功，提前退出 */

    /* 分配堆疊空間，最多需要 len 個位置（全為開括號的情況） */
    char* stack = (char*)malloc(len * sizeof(char));
    int top = -1;                       /* 堆疊頂部索引，-1 表示空堆疊 */

    for (int i = 0; i < len; i++) {
        char c = s[i];                  /* 當前處理的字元 */

        /* 開括號：直接推入堆疊 */
        if (c == '(' || c == '[' || c == '{') {
            stack[++top] = c;           /* top 先加一再存入，確保指向最新元素 */
        } else {
            /* 閉括號：堆疊為空表示沒有對應的開括號 */
            if (top == -1) {
                free(stack);            /* 釋放記憶體後回傳 */
                return false;
            }

            char last = stack[top--];   /* 彈出堆疊頂部元素，top 減一 */

            /* 檢查閉括號與彈出的開括號是否為同一類型 */
            if ((c == ')' && last != '(') ||
                (c == ']' && last != '[') ||
                (c == '}' && last != '{')) {
                free(stack);            /* 不匹配，釋放記憶體後回傳 false */
                return false;
            }
        }
    }

    bool result = (top == -1);          /* 堆疊為空表示所有括號都已配對 */
    free(stack);                        /* 釋放動態分配的記憶體 */
    return result;
}
